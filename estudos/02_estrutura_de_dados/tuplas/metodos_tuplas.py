# Metodos utilizados em tuplas
# Metodo count - conta quantas vezes o elemnto aparece na tupla
numeros = (1, 1, 2, 2, 3)
print(numeros.count(1))
print(numeros.count(2))
print(numeros.count(3))

# Metodo index - qual posicao um objeto esta
print(numeros.index(3))

# Metodo len - tamanho total da tupla
print(len(numeros))